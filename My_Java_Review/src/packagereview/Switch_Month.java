package packagereview;

public class Switch_Month {

	public static void main(String[] args) {
	
		String month="may";
		switch(month)
		{
		case "january":System.out.println("month is january");	
		break;
		case "february":System.out.println("month is february");
		break;
		case "march":System.out.println("month is march");
		break;
		default:System.out.println("invalid");
		
		}

	}

}
